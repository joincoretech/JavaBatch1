package class3;

public class NameingConvention {
	public static void main(String[] args) {
		
		
		// we never use java keyword for naming   public  static final,
		
		//int public = 2;
		
		
		// when we name the variables, we should not have space between two words.
		
		//String myLast Name= "Hassan";// this is error we have space in the name.
		
		
		// we use camel case for name the variables
		
		String numberButItIsString= "10";
		int num=5;
		System.out.println(numberButItIsString+num);
		
		int num2= 2;
		int num3 = 4;
		
		System.out.println(num2*num3);
		
		//Addition sign Is +
		System.out.println(2+4);
		
	}

}
