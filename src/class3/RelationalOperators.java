package class3;

public class RelationalOperators {
	public static void main(String[] args) {

		// Relational Operators are > , < , >=, <= , ==, !=

		int number1 = 10;
		int number2 = 5;
		System.out.println(number1 > number2);// true
		System.out.println(number1 < number2);// false
		System.out.println(number1 >= number2);// true
		System.out.println(number1 <= number2);// false
		System.out.println(number1 == number2); // false
		System.out.println(number1 != number2); // true

	}

}

